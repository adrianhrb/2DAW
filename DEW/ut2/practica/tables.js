function tabla7() {
    for (i = 0; i <= 10; i++) {
      var res = 7 * i;
      console.log("7 * " + i + " = " + res);
    }
  }
  
  function tablaSuma8() {
    var i = 0;
    while (i <= 10) {
      var res = 8 + i;
      console.log("8 + " + i + " = " + res);
      i++;
    }
  }
  
  function tablaDivide9() {
    var i = 0;
    do {
      var res = 9 / i;
      console.log("9 / " + i + " = " + res);
      i++;
    } while (i <= 10);
  }